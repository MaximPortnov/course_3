DROP TABLE IF EXISTS Products CASCADE;
DROP TABLE IF EXISTS Suppliers CASCADE; 
DROP TABLE IF EXISTS Customers CASCADE; 
DROP TABLE IF EXISTS Orders   CASCADE;
DROP TABLE IF EXISTS OrderDetails CASCADE;
DROP TABLE IF EXISTS Warehouses CASCADE; 
DROP TABLE IF EXISTS Sales    CASCADE;
DROP TABLE IF EXISTS Finances CASCADE;
DROP TABLE IF EXISTS Stock    CASCADE;
DROP TABLE IF EXISTS SupplierProducts CASCADE;


-- Создание таблицы Товаров
CREATE TABLE IF NOT EXISTS Products (
    ProductID SERIAL PRIMARY KEY,
    Name VARCHAR(255) NOT NULL,
    SportType VARCHAR(100),
    Characteristics TEXT,
    Price DECIMAL(10, 2) NOT NULL,
    QuantityInStock INT NOT NULL
);

-- Создание таблицы Поставщиков
CREATE TABLE IF NOT EXISTS Suppliers (
    SupplierID SERIAL PRIMARY KEY,
    CompanyName VARCHAR(255) NOT NULL,
    Address TEXT,
    Phone VARCHAR(20),
    SupplyConditions TEXT
);

-- Создание таблицы Клиентов
CREATE TABLE IF NOT EXISTS Customers (
    CustomerID SERIAL PRIMARY KEY,
    FullName VARCHAR(255) NOT NULL,
    ContactInfo TEXT,
    Email VARCHAR(100)
);

-- Создание таблицы Заказов
CREATE TABLE IF NOT EXISTS Orders (
    OrderID SERIAL PRIMARY KEY,
    OrderDate DATE NOT NULL,
    DeliveryDate DATE,
    Status VARCHAR(50),
    CustomerID INT,
    FOREIGN KEY (CustomerID) REFERENCES Customers(CustomerID)
);

-- Создание таблицы Список товаров в заказе (вспомогательная таблица)
CREATE TABLE IF NOT EXISTS OrderDetails (
    OrderDetailID SERIAL PRIMARY KEY,
    OrderID INT NOT NULL,
    ProductID INT NOT NULL,
    Quantity INT NOT NULL,
    FOREIGN KEY (OrderID) REFERENCES Orders(OrderID),
    FOREIGN KEY (ProductID) REFERENCES Products(ProductID)
);

-- Создание таблицы Складов
CREATE TABLE IF NOT EXISTS Warehouses (
    WarehouseID SERIAL PRIMARY KEY,
    Name VARCHAR(255) NOT NULL,
    Address TEXT,
    Capacity INT
);

-- Создание таблицы Продаж
CREATE TABLE IF NOT EXISTS Sales (
    SaleID SERIAL PRIMARY KEY,
    ProductID INT NOT NULL,
    QuantitySold INT NOT NULL,
    SaleDate DATE NOT NULL,
    CustomerID INT,
    FOREIGN KEY (ProductID) REFERENCES Products(ProductID),
    FOREIGN KEY (CustomerID) REFERENCES Customers(CustomerID)
);

-- Создание таблицы Финансов
CREATE TABLE IF NOT EXISTS Finances (
    FinanceID SERIAL PRIMARY KEY,
    OperationType VARCHAR(50),
    Amount DECIMAL(10, 2) NOT NULL,
    OperationDate DATE NOT NULL,
    Description TEXT
);

-- Создание вспомогательных таблиц для управления складскими остатками
CREATE TABLE IF NOT EXISTS Stock (
    StockID SERIAL PRIMARY KEY,
    ProductID INT NOT NULL,
    WarehouseID INT NOT NULL,
    Quantity INT NOT NULL,
    FOREIGN KEY (ProductID) REFERENCES Products(ProductID),
    FOREIGN KEY (WarehouseID) REFERENCES Warehouses(WarehouseID)
);

-- Создание таблицы ТоварПоставщик
CREATE TABLE IF NOT EXISTS SupplierProducts (
    SupplierProductID SERIAL PRIMARY KEY,
    SupplierID INT NOT NULL,
    ProductID INT NOT NULL,
    QuantitySupplied INT NOT NULL,
    SupplyPrice DECIMAL(10, 2) NOT NULL,
    SupplyDate DATE NOT NULL,
    FOREIGN KEY (SupplierID) REFERENCES Suppliers(SupplierID),
    FOREIGN KEY (ProductID) REFERENCES Products(ProductID)
);

CREATE TABLE IF NOT EXISTS FinanceSupplierProducts (
    FinanceSupplierProductID SERIAL PRIMARY KEY,
    FinanceID INT NOT NULL,
    SupplierProductID INT NOT NULL,
    FOREIGN KEY (FinanceID) REFERENCES Finances(FinanceID),
    FOREIGN KEY (SupplierProductID) REFERENCES SupplierProducts(SupplierProductID)
);

CREATE TABLE IF NOT EXISTS FinanceSales (
    FinanceSaleID SERIAL PRIMARY KEY,
    FinanceID INT NOT NULL,
    SaleID INT NOT NULL,
    FOREIGN KEY (FinanceID) REFERENCES Finances(FinanceID),
    FOREIGN KEY (SaleID) REFERENCES Sales(SaleID)
);

